for i in range(3):   # This draws the logo 3 times
    pencolor("red")
    fillcolor("lightpink")
    begin_fill()
    forward(50)
    right(90)
    forward(25)
    right(90)
    forward(75)
    right(90)
    forward(75)
    right(90)
    forward(75)
    right(90)
    forward(25)
    right(90)
    forward(50)
    left(90)
    forward(25)
    end_fill()

    penup()
    left(90)
    forward(150)

    pendown()


